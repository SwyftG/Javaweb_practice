package javabean;
public class VeInfo {
	private String VhcLcs;
	private String VhcForm;
	private String OilType;
	private float OilCpt;
	private float LoadCpt;
	private String VhcType;
	private boolean VhcSpc;
	private String VhcRmk;
	
	public String getVhcLcs() {
		return VhcLcs;
	}
	public void setVhcLcs(String vhcLcs) {
		VhcLcs = vhcLcs;
	}
	public String getVhcForm() {
		return VhcForm;
	}
	public void setVhcForm(String vhcForm) {
		VhcForm = vhcForm;
	}
	public String getOilType() {
		return OilType;
	}
	public void setOilType(String oilType) {
		OilType = oilType;
	}
	public float getOilCpt() {
		return OilCpt;
	}
	public void setOilCpt(float oilCpt) {
		OilCpt = oilCpt;
	}
	public float getLoadCpt() {
		return LoadCpt;
	}
	public void setLoadCpt(float loadCpt) {
		LoadCpt = loadCpt;
	}
	public String getVhcType() {
		return VhcType;
	}
	public void setVhcType(String vhcType) {
		VhcType = vhcType;
	}
	public boolean isVhcSpc() {
		return VhcSpc;
	}
	public void setVhcSpc(boolean vhcSpc) {
		VhcSpc = vhcSpc;
	}
	public String getVhcRmk() {
		return VhcRmk;
	}
	public void setVhcRmk(String vhcRmk) {
		VhcRmk = vhcRmk;
	}
}
